class TestGetGenerationStatus:
    """Testes para GET /api/v1/generate/{request_id}"""

    def test_get_generation_status_queued(self, client):
        """Testa status imediatamente após criação (deve ser queued ou processing)."""
        # Criar requisição
        payload = {
            "ticket_id": "test-ticket-003",
            "template_id": "microservice-python-v1",
            "parameters": {"service_name": "test", "language": "python"}
        }
        create_response = client.post("/api/v1/generate", json=payload)
        request_id = create_response.json()["request_id"]

        # Obter status (imediatamente, pode ainda estar queued)
        response = client.get(f"/api/v1/generate/{request_id}")

        assert response.status_code == 200
        data = response.json()
        assert data["request_id"] == request_id
        assert data["status"] in ["queued", "processing", "completed"]
        assert "created_at" in data
        assert "updated_at" in data

    def test_get_generation_status_completed(self, client):
        """Testa status após processamento (aguardar completion)."""
        # Criar requisição
        payload = {
            "ticket_id": "test-ticket-004",
            "template_id": "microservice-python-v1",
            "parameters": {"service_name": "test", "language": "python"}
        }
        create_response = client.post("/api/v1/generate", json=payload)
        request_id = create_response.json()["request_id"]

        # Aguardar processamento
        asyncio.sleep(3)

        # Obter status
        response = client.get(f"/api/v1/generate/{request_id}")

        assert response.status_code == 200
        data = response.json()
        assert data["status"] == "completed"
        assert len(data["artifacts"]) > 0
        assert data["artifacts"][0]["type"] == "code"
        assert "content_hash" in data["artifacts"][0]
        assert data["code_preview"] is not None
        assert "Generated by Neural Code Forge" in data["code_preview"]

    def test_get_generation_status_not_found_404(self, client):
        """Testa erro quando request_id não existe."""
        response = client.get("/api/v1/generate/nonexistent-id")

        assert response.status_code == 404
        assert "not found" in response.json()["detail"].lower()


