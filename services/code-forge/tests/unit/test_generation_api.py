class TestGenerationApiIntegration:
    """Testes de integração do fluxo completo"""

    def test_full_generation_flow(self, client):
        """Testa fluxo completo: criar → aguardar → status."""
        # 1. Criar requisição
        payload = {
            "ticket_id": "integration-test-001",
            "template_id": "microservice-python-v1",
            "parameters": {
                "service_name": "integration-service",
                "language": "python",
                "description": "Integration test service"
            }
        }
        create_response = client.post("/api/v1/generate", json=payload)
        assert create_response.status_code == 202

        request_id = create_response.json()["request_id"]

        # 2. Aguardar processamento
        import time
        time.sleep(3)

        # 3. Verificar status completo
        status_response = client.get(f"/api/v1/generate/{request_id}")
        assert status_response.status_code == 200

        data = status_response.json()
        assert data["status"] == "completed"
        assert len(data["artifacts"]) > 0
        assert "integration-service" in data["code_preview"]

    def test_concurrent_generations(self, client):
        """Testa múltiplas gerações concorrentes."""
        import time

        # Criar 3 requisições concorrentes
        request_ids = []
        for i in range(3):
            payload = {
                "ticket_id": f"concurrent-test-{i}",
                "template_id": "microservice-python-v1",
                "parameters": {"service_name": f"service-{i}", "language": "python"}
            }
            response = client.post("/api/v1/generate", json=payload)
            assert response.status_code == 202
            request_ids.append(response.json()["request_id"])

        # Aguardar processamento
        time.sleep(5)

        # Verificar que todas completaram
        for request_id in request_ids:
            response = client.get(f"/api/v1/generate/{request_id}")
            assert response.status_code == 200
            data = response.json()
            assert data["status"] in ["completed", "failed"]  # Pode falhar, não deve crashar
